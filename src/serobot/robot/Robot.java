package serobot.robot;

public class Robot
{

	
	private int xCoord;
	private int yCoord;
	
	//constructor
	public Robot()
	{
	this.xCoord=xCoord;
	this.yCoord=yCoord;
	}
	
	public int getXCoord()
	{
		return xCoord;
	}
	
	public int getYCoord()
	{
		return yCoord;
	}
	
	public void setCoords(int xCoordinate,int yCoordinate)
	{
		xCoord=xCoordinate;
		yCoord=yCoordinate;
	}
	
	
	
}
